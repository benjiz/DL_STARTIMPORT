    private ArrayList<String> getALfromList (JList jL){
        ArrayList<String> aLObj = new ArrayList<String>();
            int size = jL.getModel().getSize();               
                for (int i=0; i < size; i++){
                    String getBib = jL.getModel().getElementAt(i).toString();
                    aLObj.add(getBib);
                }
        
        return   aLObj;
    }
    public  boolean chooseDialog(JTextField jTF){
        try{
        JFileChooser chooser = new JFileChooser();
        chooser.showOpenDialog(null);                   
        if (chooser.getSelectedFile().length()>0){
            File file = chooser.getSelectedFile(); 
            String filename = file.getAbsolutePath();
            jTF.setText(filename);
            return true;
        }
        else{
            return false;
            }
        }
        catch (Exception e){
            return false;
        }
  
    }
