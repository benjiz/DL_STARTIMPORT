    private void jBT_browseMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jBT_browseMouseClicked
        if(chooseDialog(jTF_mainfilepath)){
            
            ReadExcelFile readEF = new ReadExcelFile();
            
            String filepath = jTF_mainfilepath.getText();
            int sSize = readEF.getSheetsSize(filepath);
            jLB_savepath.setText(sSize+"");
            int size = jL_biblist.getModel().getSize();
                ArrayList<String> aLBib = new ArrayList<String>();
                for (int i=0; i < size; i++){
                    String getBib = jL_biblist.getModel().getElementAt(i).toString();
                    aLBib.add(getBib);
                }
                
            for (int k = 0 ; k < sSize ; k++){
                matchList.clear();
                ArrayList<List<String>> iVec = readEF.ReadExcel(filepath,k);               
                ArrayList<String> nVec=  readEF.matchBibNo(iVec,aLBib);
                for (int i = 0 ;i < nVec.size();i++){
                    matchList.addElement(nVec.get(i));
                }
                jL_matchlist.removeAll();
                jL_matchlist.setModel(matchList);
                jLB_savepath.setText(k+"");
                ArrayList<String> saveObj  = getALfromList(jL_matchlist);
                WriteText wt = new WriteText();
                wt.write(saveObj, "C:\\Users\\9119\\Documents\\"+(k+1)+".csv");
                
            }
       }
        
    }//GEN-LAST:event_jBT_browseMouseClicked

